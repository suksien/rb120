# Using the code from the previous exercise, 
# create an instance of Cat and assign it to a variable named kitty.

class Cat; end

kitty = Cat.new

# key point: how to instantiate an object

=begin
To create an instance or an object of a class, we invoke the class method `::new` on the
class and assign the object to a variable. In this case, we invoke `::new` on the `Cat` class, 
  and assign the `Cat` object to the `kitty` variable. 
=end